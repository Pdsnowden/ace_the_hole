    <div class="faqQuestion">Can I use a personal music devise while running?</div>
     <div class="faqAnswer"><p>During any running segments, we prefer for athletes
        to NOT use music devices to maximize safety, assure a fair competitive
        environment and assure athletes can hear all course marshal instructions.
